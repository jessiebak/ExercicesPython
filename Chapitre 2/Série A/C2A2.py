# Ecrivez un bout de code qui écrit "Au revoir" 998 fois. Devant chaque "Au revoir", le numéro de l'itération est écrite.
# Cela devra donner quelque chose comme :
# 1) Au revoir
# 2) Au revoir
# 3) Au revoir
# ...
# 998) Au revoir

i=0 
while (i<998):
    print(f"{i+1}) Au revoir !")
    i+=1 
